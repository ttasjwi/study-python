# 논리 연산자 : not, and, or

# not
print(not True)
print(not False)

x = 10
print("x < 20 = ", x < 20)
print("not (x < 10) = ", not (x < 10))

# and : 둘다 true일 때 true
# or : 둘 중 하나가 true 일때 true
x = 20
print('x = ', x)
print('10 < x and x < 30 = ', 10 < x and x > 30)
print('10 < x or x < 30 = ', 10 < x or x < 30)
