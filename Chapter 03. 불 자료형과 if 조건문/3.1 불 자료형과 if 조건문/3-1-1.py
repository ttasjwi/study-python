# 불 자료형 만들기 : 비교 연산자 사용
print(10 == 100)
print(10 != 100)
print(10 < 100)
print(10 > 100)
print(10 <= 100)
print(10 >= 100)

# python에서도 문자열을 비교연산자로 비교 가능하고, 사전 순서로 대소관계를 가진다.
print('가방' == '가방')
print('가방' != '하마')
print('가방' < '하마')
print('가방' > '하마')

# 파이썬에서는 범위를 &&로 엮지 않고 비교할 수 있다
x = 2
print(1 < x < 3)
