
# int : 소숫점이 없는 숫자 (정수형)
print(273)
print(type(273)) # int

# float : 소숫점이 있는 숫자 (실수형 : 부동소숫점)
print(52.273)
print(type(52.273)) # float

print(0)
print(type(0))

print(0.0)
print(type(0.0))


# 지수표현 가능
print(0.52273e2) # 52.273
print(type(0.52273e2))

print(0.52273e-2) # 0.0052273
print(type(0.52273e-2))
