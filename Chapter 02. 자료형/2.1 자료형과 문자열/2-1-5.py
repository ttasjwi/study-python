
# 문자열 선택 연산자(인덱싱) []

print('[]는 문자열 선택 연산자다')
print('안녕하세요'[0])
print('안녕하세요'[1])
print('안녕하세요'[2])
print('안녕하세요'[3])
print('안녕하세요'[4])

# IndexError : 범위를 벗어난 인덱스를 지정할 경우 에러
